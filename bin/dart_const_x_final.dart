void main(List<String> arguments) {
  /* const => Variaveis com valores que sao imutaveis, recebe o valor quando a variavel for criada */
  const String nome1 = "Dart";
  print(nome1);
  /* final => Atribuição unica para as variaveis, so recebe um valor, nao precisa receber esse valor quando for criada, depois que recebe um valor ela fica imutavel */
  final String nome2;
  nome2 = "Flutter";
  print(nome2);
}
